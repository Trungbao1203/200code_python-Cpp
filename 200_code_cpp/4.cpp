// Tính tích hai số: Nhập hai số và in ra tích của chúng.

#include <iostream>
using namespace std;

int main() {
    // Khai báo hai biến
    float a, b;

    // Nhập hai số từ bàn phím
    cout << "Nhap a: ";
    cin >> a;
    cout << "Nhap b: ";
    cin >> b;

    // Tính tích hai số
    float P = a*b;

    // In kết quả
    cout << "Tich cua hai so la: " << P << endl;

    return 0;
}
    